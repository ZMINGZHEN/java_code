package string;
/**
 * char charAt(int index)
 * 获取当前字符串中指定位置的字符
 * @author Xiloer
 *
 */
public class CharAtDemo {
	public static void main(String[] args) {
		//            0123456789012345
		String str = "thinking in java";
		char c = str.charAt(9);
		System.out.println(c);//i
		
	}
}




